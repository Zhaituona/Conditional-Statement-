package Array;

public class UseOf2DimArray {
    public static void main(String[] args) {
        int [][] x = new int [3][4] ;
        int [][] x1 = {{12,3,6,7},{3,5,6,7},{11,4,6,7,8}};

        System.out.println(x1[0][0]);
        System.out.println(x1[2][0]);
        System.out.println(x1[2][4]);
        System.out.println(x1[1][2]);

    }
}
